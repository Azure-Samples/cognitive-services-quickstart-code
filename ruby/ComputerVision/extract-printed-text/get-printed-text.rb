require 'net/http'

uri = URI('PASTE_YOUR_COMPUTER_VISION_ENDPOINT_HERE' + '/vision/v3.1/ocr')
uri.query = URI.encode_www_form({
    # Request parameters
    'language' => 'unk',
    'detectOrientation' => 'true'
})

request = Net::HTTP::Post.new(uri.request_uri)

# Request headers
# Replace <key> with your valid key.
request['Ocp-Apim-Subscription-Key'] = 'PASTE_YOUR_COMPUTER_VISION_KEY_HERE'
request['Content-Type'] = 'application/json'

request.body =
    "{\"url\": \"https://upload.wikimedia.org/wikipedia/commons/thumb/a/af/" +
    "Atomist_quote_from_Democritus.png/338px-Atomist_quote_from_Democritus.png\"}"

response = Net::HTTP.start(uri.host, uri.port, :use_ssl => uri.scheme == 'https') do |http|
    http.request(request)
end

puts response.body